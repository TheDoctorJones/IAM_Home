// Helper functions
export function getPartyById(id: string): Party | undefined {
  return parties.find((party) => party.id === id)
}

export function getPartyByName(name: string): Party | undefined {
  return parties.find((party) => party.name.toLowerCase() === name.toLowerCase())
}

export function getPartiesByType(type: Party["type"]): Party[] {
  return parties.filter((party) => party.type === type)
}

export function getPartiesByCategory(category: Party["category"]): Party[] {
  return parties.filter((party) => party.category === category)
}

export function getPartiesByIndustryType(industryType: Party["industryType"]): Party[] {
  return parties.filter((party) => party.industryType === industryType)
}

export function getContactById(contactId: string): { contact: PartyContact; party: Party } | undefined {
  for (const party of parties) {
    const contact = party.contacts.find((c) => c.id === contactId)
    if (contact) {
      return { contact, party }
    }
  }
  return undefined
}

export function getPrimaryContactForParty(partyId: string): PartyContact | undefined {
  const party = getPartyById(partyId)
  return party?.contacts.find((contact) => contact.isPrimary && contact.isActive)
}

